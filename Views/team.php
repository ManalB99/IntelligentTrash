		<h1>Presentation de la poubelle intelligente</h1>
		<p>
        La poubelle pourra grâce à une IA trier automatiquement les déchets, différent capteurs permettront de détecter le déchets et ensuite la poubelle pourra mettre le déchet dans le compartiment correspondant (Verre, Organique, Jaune , Autres). Pour que l’IA puisse s'aider, une grande base de données avec différents éléments sera ajoutée. Elle aura aussi la possibilité de compacter les déchets, cela permettra de stocker plus de déchets contrairement à une poubelle normale.

        Cette poubelle sera dite “Connecté” car elle sera reliée au réseau internet et plus précisément à une application, cette application sera accessible par deux utilisateurs différents.
        Par la mairie de la ville qui aura les droits administrateur. La mairie aura la possibilité de connaître tous les détails de chaque poubelle (nombre de déchets, % de déchets par poubelle, % de type de déchets par poubelles, Nombre de poubelles dans la ville, etc ...). Ces informations permettront à la mairie de connaître les quartiers les plus fréquentés grâce à la quantité de déchets par jour dans les poubelles, mais aussi elle pourra installer de nouvelle poubelle ou des nouvelle technologie éco responsable en fonction des données récoltées. 
        Par les citoyens de la ville. Les habitants auront aussi accès à l’application mais pour une autre utilité complètement différentes, l’application permettra à l'utilisateur de bénéficier de points pour chaque déchets déposés dans la poubelle. A partir d’un certain nombres de points, l’utilisateur bénéficiera d’un badges, chaque badges permet d’avoir un certain niveau de réductions sur les infrastructures de la ville (Piscine, Zoo, Cinéma, Musée). L’utilisateur aura aussi la possibilité de consulter la carte de la ville sur son téléphone avec les différentes poubelles présentes dans les quartiers, il pourra donc voir les différentes poubelles près de chez lui.
        Les avantages de cette poubelle c’est qu’elle permet aux utilisateurs d’être récompensés pour leur bon gestes, par conséquent ce réglera le problème de déchets dans la rue ce qui rendra service à la ville. De plus les poubelles trieront automatiquement chaque détritus par conséquent cela évitera un main d'œuvre en plus pour la ville. 
        Par conséquent, cette poubelle est l’avenir de nos rues, pour leur propreté et leur évolution de la technologie.	
		</p>

